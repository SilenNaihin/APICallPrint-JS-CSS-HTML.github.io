let monday23 = [];
let tuesday24 = [];
let wednesday25 = [];
let thursday26 = [];

sortedData.forEach((object)=>{
    actualDate = new Date(object.TimeSlot.StartTime.UTC)
    if(actualDate.getDate()==23){
        monday23.push(object);
    } else if(actualDate.getDate()==24){
        tuesday24.push(object);
    } else if(actualDate.getDate()==25){
        wednesday25.push(object);
    } else if(actualDate.getDate()==26){
        thursday26.push(object);
    }
});

