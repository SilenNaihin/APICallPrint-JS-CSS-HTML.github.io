schedule.tuesday = tuesday24
schedule.wednesday = wednesday25
schedule.thursday = thursday26

// Add class to monday

for(let day in schedule){
    let dayArr = Array.from(`${day}`);
    let letter = "";
    let listDay = "";
    dayArr.forEach(function(char, i){ 
        if(i<1){
            let firstLetter = char.toUpperCase();
            letter += `<div class='titleLetter'><span class="letter upper" data-letter="${char}">${firstLetter}</span>`;
        } else if(i==dayArr.length-1){
            letter += `<span class="letter" data-letter="${char}">${char}</span></div>`;
        } else{
            letter += `<span class="letter" data-letter="${char}">${char}</span>`;
        };
    });
    
    
    schedule[day].forEach((session)=>{
        let date = new Date(session.TimeSlot.StartTime.UTC); 
        let date12 = date.toLocaleString('en-US', { hour: 'numeric', minute: 'numeric', hour12: true })

        listDay += `<div class="box-single"><h4 class='sessionTitle'>${session.Title}</h4><h4 class = 'size1'>${date12}<span class='trackTitle-format'> @ ${session.Track.Title}</span></h4><h4 class='desc-format'>Description: ${session.Description}</h4></div>`
    });

    wrapper.innerHTML+=`
    <div id='${day}' class='dayOfWeek'>
        <div class='title column'>
              
            ${letter} 
        </div>
        <div  class="box-track">
            
            ${listDay}
        </div>
    </div>`;
};


